CREATE TABLE PESSOA(
    ID NUMBER(5),
    NOME CHAR(20),
    IDADE NUMBER(3),
    GENERO CHAR(1),
    SEGUIDORES NUMBER(10), -- numero de seguidores
    SALARIO NUMBER(10),
    PRIMARY KEY (ID)
);

CREATE TABLE NACIONALIDADE(
    PAIS VARCHAR(50),
    ID_PESSOA NUMBER(5),
    FOREIGN KEY (ID_PESSOA) REFERENCES PESSOA(ID),
    CONSTRAINT ID_NACIONALIDADE PRIMARY KEY (PAIS, ID_PESSOA)
);

CREATE TABLE CASTER(
    ID_CASTER NUMBER(5),
    FUNCAO VARCHAR(30),
    POPULARIDADE NUMBER(4, 2), -- Em porcentagem
    PRIMARY KEY(ID_CASTER),
    FOREIGN KEY (ID_CASTER) REFERENCES PESSOA(ID)
);

CREATE TABLE EQUIPE(
    ID_EQUIPE NUMBER(5),
    ID_CEO NUMBER(5) UNIQUE NOT NULL,
    DATA_CRIACAO DATE,
    NOME CHAR (20),
    PRIMARY KEY (ID_EQUIPE),
    FOREIGN KEY (ID_CEO) REFERENCES PESSOA(ID)
);

CREATE TABLE JOGADOR(
    ID_JOGADOR NUMBER(5),
    FUNCAO CHAR(3) NOT NULL,
    EH_RESERVA NUMBER(1) CHECK (EH_RESERVA = 0 OR EH_RESERVA = 1),
    NICK_LOL VARCHAR(30) NOT NULL,
    ID_EQUIPE NUMBER(5),
    PRIMARY KEY (ID_JOGADOR),
    FOREIGN KEY (ID_EQUIPE) REFERENCES EQUIPE(ID_EQUIPE),
    FOREIGN KEY (ID_JOGADOR) REFERENCES PESSOA(ID)
);

CREATE TABLE COACH(
    ID_COACH NUMBER(5),
    TIPO VARCHAR(30),
    ID_HEAD_COACH NUMBER(5),
    ID_EQUIPE NUMBER(5),
    PRIMARY KEY (ID_COACH),
    FOREIGN KEY (ID_HEAD_COACH) REFERENCES COACH(ID_COACH),
    FOREIGN KEY (ID_COACH) REFERENCES PESSOA(ID),
    FOREIGN KEY (ID_EQUIPE) REFERENCES EQUIPE(ID_EQUIPE)
);

CREATE TABLE CAMPEONATO(
    ID_CAMPEONATO NUMBER(6),
    ID_MVP NUMBER(5),
    QNTD_EQUIPES NUMBER(3),
    PRIMARY KEY (ID_CAMPEONATO),
    FOREIGN KEY (ID_MVP) REFERENCES JOGADOR(ID_JOGADOR)
);

CREATE TABLE PAUSE(
    COD NUMBER(5),
    MOTIVO VARCHAR(100),
    TEMPO_MIN NUMBER(30),
    PRIMARY KEY (COD)
);

CREATE TABLE PREMIACAO(
    POSICAO NUMBER(2),
    ID_CAMPEONATO NUMBER(5),
    VALOR NUMBER(15),
    FOREIGN KEY (ID_CAMPEONATO) REFERENCES CAMPEONATO(ID_CAMPEONATO),
    CONSTRAINT ID_PREMIACAO PRIMARY KEY (POSICAO, ID_CAMPEONATO)
);

CREATE TABLE JOGO(
    ID_EQUIPE1 NUMBER(5),
    ID_EQUIPE2 NUMBER(5) NOT NULL,
    ID_CAMPEONATO NUMBER(5),
    DIA DATE,
    GANHADOR NUMBER(5),
    UNIQUE (DIA, ID_EQUIPE2),
    CONSTRAINT ID_JOGO PRIMARY KEY (ID_EQUIPE1, ID_CAMPEONATO, DIA),
    FOREIGN KEY (ID_EQUIPE1) REFERENCES EQUIPE(ID_EQUIPE),
    FOREIGN KEY (ID_EQUIPE2) REFERENCES EQUIPE(ID_EQUIPE),
    FOREIGN KEY (ID_CAMPEONATO) REFERENCES CAMPEONATO(ID_CAMPEONATO)
);

CREATE TABLE APRESENTA(
    ID_CAMPEONATO NUMBER(5),
    ID_CASTER NUMBER(5),
    CONSTRAINT ID_APRESENTA PRIMARY KEY (ID_CAMPEONATO, ID_CASTER),
    FOREIGN KEY (ID_CAMPEONATO) REFERENCES CAMPEONATO(ID_CAMPEONATO),
    FOREIGN KEY (ID_CASTER) REFERENCES CASTER(ID_CASTER)
);

CREATE TABLE OCORRE(
    ID_EQUIPE1 NUMBER(5),
    ID_CAMPEONATO NUMBER(5),
    DIA DATE,
    COD NUMBER(5),
    CONSTRAINT ID_OCORRE PRIMARY KEY (ID_EQUIPE1, ID_CAMPEONATO, DIA, COD),
    CONSTRAINT ID_JOGO_OCORRE FOREIGN KEY (ID_EQUIPE1, ID_CAMPEONATO, DIA) REFERENCES JOGO(ID_EQUIPE1, ID_CAMPEONATO, DIA),
    FOREIGN KEY (COD) REFERENCES PAUSE(COD)
);

